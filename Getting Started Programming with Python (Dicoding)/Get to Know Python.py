# --- Pengenalan Python ---

# Python adalah bahasa pemrograman multifungsi yang dirilis pada tahun 1991 oleh Guido van Rossum (GvR).
# Beliau membuat Python sebagai bahasa pemrograman yang mudah dibaca dan dimengerti (readable)
# serta memiliki kemampuan penanganan kesalahan (exception handling). Berdasarkan tujuan tersebut,
# Guido van Rossum berhasil  menjadikan Python sebagai bahasa pemrograman yang dapat diimplementasikan
# ke dalam berbagai sektor. Python dapat digunakan untuk membangun website (server-side), analisis data,
# hingga pembelajaran mesin (machine learning).

# Python memiliki ciri khas tersendiri sebagai salah satu pemrograman populer.
# Salah satu ciri khas yang paling dikenal adalah Python tidak mewajibkan penggunaan titik koma
# atau semi colon (;) pada setiap akhir kode programnya.

# Python versi 2 dirilis pada Oktober tahun 2000,
# beberapa fitur di antaranya adalah garbage collector dan memory management.

# Python versi 3 dirilis pada Desember tahun 2008,
# Versi 3 ini tidak bersifat backward-compatible

# Python memiliki prinsip readable, consistent, & explicit.

# Beberapa di antara Python system:
# Python Enhancement Proposals (PEP) dan Python Software Foundation (PSF).

# Python merupakan tiga peringkat teratas bahasa pemrograman yang dipilih
# oleh para programmer atau pemula untuk belajar kode/program.

# Python dapat digunakan dalam mengakomodasi berbagai gaya pemrograman,
# di antaranya: terstruktur, prosedural, berorientasi objek, dan fungsional.

# Python dapat dimanfaatkan dalam berbagai sektor berikut:
# 1) Web development (server-side);
# 2) Software development;
# 3) Mathematics & data science;
# 4) Machine learning;
# 5) System scripting;
# 6) Internet of Things (IoT) development; dan lain lain.